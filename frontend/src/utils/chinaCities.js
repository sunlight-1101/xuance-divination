function shortProvinceName(name) {
  return String(name || '')
    .replace('特别行政区', '')
    .replace('壮族自治区', '')
    .replace('回族自治区', '')
    .replace('维吾尔自治区', '')
    .replace('自治区', '')
    .replace(/[省市]$/, '')
}

function shortCityName(name) {
  return String(name || '')
    .replace('土家族苗族自治州', '')
    .replace('布依族苗族自治州', '')
    .replace('苗族侗族自治州', '')
    .replace('哈尼族彝族自治州', '')
    .replace('傣族自治州', '')
    .replace('白族自治州', '')
    .replace('藏族自治州', '')
    .replace('彝族自治州', '')
    .replace('蒙古自治州', '')
    .replace('哈萨克自治州', '')
    .replace('回族自治州', '')
    .replace('蒙古族藏族自治州', '')
    .replace('哈尼族彝族自治州', '')
    .replace('柯尔克孜自治州', '')
    .replace('地区', '')
    .replace(/[市州盟]$/, '')
}

function cityItem(provinceName, cityName) {
  const shortName = shortCityName(cityName)
  return {
    name: shortName,
    fullName: cityName,
    longitude: cityLongitude[shortName] || provinceLongitude[provinceName] || 120
  }
}

function buildProvinceOptions() {
  const byProvince = new Map()
  for (const item of provinceData) {
    const provinceName = shortProvinceName(item.name || item.n)
    byProvince.set(item.province || item.p, {
      name: provinceName,
      fullName: item.name || item.n,
      cities: []
    })
  }

  for (const item of cityData) {
    const fullCityName = item.name || item.n
    if (String(fullCityName).includes('直辖县级行政区划')) continue
    const province = byProvince.get(item.province || item.p)
    if (!province) continue
    province.cities.push(cityItem(province.name, fullCityName))
  }

  for (const item of municipalityCities) {
    const province = [...byProvince.values()].find(provinceItem => provinceItem.name === item.province)
    if (!province) continue
    if (!province.cities.some(city => city.name === item.city)) {
      province.cities.unshift(cityItem(province.name, item.city))
    }
  }

  return [...byProvince.values()]
    .filter(province => province.cities.length)
    .map(province => ({
      ...province,
      cities: province.cities.sort((a, b) => a.name.localeCompare(b.name, 'zh-Hans-CN'))
    }))
}

export const provinceOptions = buildProvinceOptions()
